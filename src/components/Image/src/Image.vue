<template>
	<div :class="$s.ImageWrapper">
		<m-skeleton-block
			v-if="!loaded"
			:class="[
				$s.Image,
				$s[`shape_${resolvedShape}`],
			]"
		/>
		<m-transition-fade-in>
			<img
				v-if="loaded"
				:class="[
					$s.Image,
					$s[`shape_${resolvedShape}`],
				]"
				:src="src"
				:srcset="srcset"
				v-bind="$attrs"
				v-on="$listeners"
			>
		</m-transition-fade-in>
	</div>
</template>

<script>
import { MTransitionFadeIn } from '@square/maker/components/TransitionFadeIn';
import { MSkeletonBlock } from '@square/maker/components/Skeleton';
import { MThemeKey, defaultTheme, resolveThemeableProps } from '@square/maker/components/Theme';

function SharedIntersectionObserver() {
	const callbacks = new WeakMap();
	const o = new IntersectionObserver((entries) => {
		entries.forEach((entry) => callbacks.get(entry.target)(entry));
	});

	return {
		watch(element, callback) {
			callbacks.set(element, callback);
			o.observe(element);
		},
		unwatch(element) {
			o.unobserve(element);
			callbacks.delete(element);
		},
	};
}

const imgCache = new Set();

let observer;

/**
 * @inheritAttrs img
 * @inheritListeners img
 */
export default {
	components: {
		MTransitionFadeIn,
		MSkeletonBlock,
	},

	inject: {
		theme: {
			default: defaultTheme(),
			from: MThemeKey,
		},
	},

	inheritAttrs: false,

	props: {
		src: {
			type: String,
			default: undefined,
		},
		srcset: {
			type: String,
			default: undefined,
		},
		shape: {
			type: String,
			default: undefined,
			validator: (shape) => ['squared', 'rounded', 'pill'].includes(shape),
		},
		lazyload: {
			type: Boolean,
			default: false,
		},
	},

	data() {
		return {
			loaded: imgCache.has(this.src + this.srcset),
		};
	},

	computed: {
		...resolveThemeableProps('image', ['shape']),
	},

	watch: {
		src: 'load',
		srcset: 'load',
	},

	mounted() {
		if (!this.lazyload) {
			this.load();
		} else {
			if (!observer) {
				observer = new SharedIntersectionObserver();
			}
			observer.watch(this.$el, ({ isIntersecting }) => {
				if (isIntersecting) {
					this.load();
				}
			});
		}
	},

	beforeDestroy() {
		observer.unwatch(this.$el);
	},

	methods: {
		load() {
			if (this.loaded || (!this.src && !this.srcset)) {
				return;
			}

			const img = new Image();

			if (this.src) {
				img.src = this.src;
			}

			if (this.srcset) {
				img.srcset = this.srcset;
			}

			img.addEventListener('load', () => {
				imgCache.add(this.src + this.srcset);
				this.loaded = true;
			});
		},
	},
};
</script>

